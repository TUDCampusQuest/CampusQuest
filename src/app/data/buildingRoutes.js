// ─── Helper: canonical key (always alphabetical) ─────────────────────────────
export function getRouteKey(idA, idB) {
    return [idA, idB].sort().join('_');
}

// ─── Lookup: get coords from A to B, reversed if needed ──────────────────────
export function getRouteCoords(fromId, toId) {
    const key = getRouteKey(fromId, toId);
    const coords = buildingRoutes[key];
    if (!coords) return null;
    // If the key was stored as TO_FROM but we want FROM_TO, reverse the array
    const [first, second] = key.split('_').join(' ').match(/\S+/g) ?? key.split('_');
    // Re-derive: if the stored key starts with fromId it's already correct
    const storedFrom = key.startsWith(fromId) || key.startsWith(fromId.charAt(0));
    // Simple check: does the first coord match the from-building's rough position?
    // We just check key order: sorted key means lower alphabetical ID is first.
    const sortedFirst = [fromId, toId].sort()[0];
    const needsReverse = sortedFirst !== fromId;
    return needsReverse ? [...coords].reverse() : coords;
}

// ─── Chain lookup: A→intermediate→B if direct doesn't exist ─────────────────
// Tries all 8 other buildings as a relay node and returns the first valid chain.
export function getChainedRoute(fromId, toId, allIds) {
    const intermediates = allIds.filter(id => id !== fromId && id !== toId);
    for (const mid of intermediates) {
        const leg1 = getRouteCoords(fromId, mid);
        const leg2 = getRouteCoords(mid, toId);
        if (leg1 && leg2) {
            // Stitch: drop the duplicate waypoint at the seam
            return [...leg1, ...leg2.slice(1)];
        }
    }
    return null;
}

// ─── Snap to nearest building from a [lng, lat] position ─────────────────────
export function snapToNearestBuilding(lngLat, locations) {
    let nearest = null;
    let minDist = Infinity;
    for (const loc of locations) {
        const [bLng, bLat] = loc.coordinates;
        const d = Math.hypot(lngLat[0] - bLng, lngLat[1] - bLat);
        if (d < minDist) { minDist = d; nearest = loc; }
    }
    return nearest;
}